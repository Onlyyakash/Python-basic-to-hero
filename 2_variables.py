name = "akash"
print(type(name))  # <class 'str'>

age = 21
print(type(age))  # <class 'int'>

height = 5.10
print(type(height))  # <class 'float'>

is_student = True
print(type(is_student))  # <class 'bool'>

# Multiple variable assignment
x, y, z = 10, 20, 30
print(x, y, z)  # 10 20 30
# Swapping variables
x, y = y, x
print(x, y)  # 20 10
# Constants
PI = 3.14
print(PI)  # 3.14
# Using variables in print statements
print("Name:", name)  # Name: akash
print("Age:", age)  # Age: 21
print("Height:", height)  # Height: 5.1
print("Is Student:", is_student)  # Is Student: True
